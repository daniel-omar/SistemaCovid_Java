    private void pintarLabel(JLabel lbl, String ruta) {
        ImageIcon imagen = new ImageIcon(ruta);
        Icon icono = new ImageIcon(
                imagen.getImage().getScaledInstance(
                        lbl.getWidth(), lbl.getHeight(), Image.SCALE_DEFAULT
                )
        );
        lbl.setIcon(icono);
        this.repaint();
    }

    private void pintarBoton(JButton btn, String ruta) {
        ImageIcon imagen = new ImageIcon(ruta);
        Icon icono = new ImageIcon(
                imagen.getImage().getScaledInstance(
                        btn.getWidth(), btn.getHeight(), Image.SCALE_DEFAULT
                )
        );
        btn.setIcon(icono);
        this.repaint();
    }

    private void inicioSesion() {
        try {

            String validar = VerificarCampos();
            if (validar != "") {
                JOptionPane.showMessageDialog(null, "Completar campo " + validar);
                return;
            }

            Persona persona = personaDAO.iniciarSesion(txtUser.getText());

            if (persona == null) {
                JOptionPane.showMessageDialog(null, "Usuario incorrecto", "Info", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            Principal principal = new Principal(persona);
            principal.setVisible(true);
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private String VerificarCampos() {
        //Verificar informaci√≥n
        if (txtUser.getText().length() <= 0) {
            return "Usuario";
        }
//        if (txtPassword.getText().length() <= 0) {
//            return "Password";
//        }
        return "";
    }

